a = (input('Sobre o que você quer saber?'))
print('Seu tipo primitivo é:', type(a))
print('Isso possui apenas espaços?',a.isspace())
print('É um número?',a.isnumeric())
print('É alfabético?',a.isalpha())
print('É alfanúmerico?',a.isalnum())
print('Está somente em maiúscula?',a.isupper())
print('Está somente em minúscula?',a.islower())
print('Está capitalizada?',a.istitle())
print('Obrigado!')
#O intuito desse código era identificar os desdobramentos da palavra, número ou letra digitada.

